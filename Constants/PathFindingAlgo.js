export class Queue {
	constructor() {
		this.items = {};
		this.frontIndex = 0;
		this.backIndex = 0;
	}

	enqueue(item) {
		this.items[this.backIndex] = item;
		this.backIndex++;
		return item + " inserted";
	}

	dequeue() {
		const item = this.items[this.frontIndex];
		delete this.items[this.frontIndex];
		this.frontIndex++;
		return item;
	}

	peek() {
		return this.items[this.frontIndex];
	}

	isEmpty() {
		if (this.peek() === undefined) {
			return true;
		}
		return false;
	}

	get printQueue() {
		return this.items;
	}
}

export class Graph {
	constructor(nodeCount) {
		this.nodeCount = nodeCount;
		this.adjList = new Map();
	}

	// Add lone node to graph
	addNode(n) {
		this.adjList.set(n, []);
	}

	addEdge(n, w, directions) {
		// Add an edge from n to w
		this.adjList.get(n).push([w, directions]);
	}

	outputGraph() {
		var keys = this.adjList.keys();

		for (var i of keys) {
			var list = this.adjList.get(i);
			var temp = "";

			for (var j of list) {
				temp += j + " ";
			}

			console.log(i + " -> " + temp);
		}
	}

	findPath(startingNode, endNode) {
		const visited = new Map();
		const priors = new Map();

		// create a visited object
		var keys = this.adjList.keys();

		for (var i of keys) {
			visited.set(i, false);
			priors.set(i, [-1]);
		}

		// Create an object for queue
		var q = new Queue();

		// add the starting node to the queue
		visited.set(startingNode, true);
		q.enqueue(startingNode);

		// loop until queue is empty
		while (!q.isEmpty()) {
			// get the element from the queue
			var getQueueElement = q.dequeue();

			// passing the current vertex to callback function
			// get the adjacent list for current vertex
			var get_List = this.adjList.get(getQueueElement);

			// loop through the list and add the element to the
			// queue if it is not processed yet
			for (var i in get_List) {
				var neigh = get_List[i][0];

				if (!visited.get(neigh)) {
					visited.set(neigh, true);

					priors.set(neigh, [getQueueElement, get_List[i][1]]);

					q.enqueue(neigh);
				}
			}
		}

		var path = [[endNode, "You have arrived."]];
		var current = endNode;

		while (priors.get(current)[0] !== -1) {
			path.unshift([priors.get(current)[0], priors.get(current)[1]]);
			current = priors.get(current)[0];
		}

		return path;
	}
}

