export function calculatePathLine(path) {
	var ret = {
		coordinates: [],
	};

	for (var i = 0; i < path.length; i++) {
		ret.coordinates.push({ latitude: path[i][0].latitude, longitude: path[i][0].longitude });
	}

	return ret;
}
